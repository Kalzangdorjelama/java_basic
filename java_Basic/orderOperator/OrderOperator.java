package orderOperator;

public class OrderOperator {
    public static void main(String[] args) {
        int x = 10 + 3 * 2; // 16
        int y = (10 + 3) * 2; // 26
        System.out.println(x);
        System.out.println(y);
        // order of operators
        // 1. ()
        // 2. * , /
        // 3. + , -

    }

}
